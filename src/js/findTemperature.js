 function findTemperature(data) {
  const temp ={
  };
  data.map(el=>{
    Object.keys(el).forEach(key=>{
      if (key === 'dt_txt') {
        if (!temp[`${new Date(el[key]).getUTCMonth()}/${new Date(el[key]).getUTCDate()}`]) {
          temp[`${new Date(el[key]).getUTCMonth()}/${new Date(el[key]).getUTCDate()}`] = {
            day: new Date(el[key]).getUTCDay(),
            temperature: []
          };
        }
        temp[`${new Date(el[key]).getUTCMonth()}/${new Date(el[key]).getUTCDate()}`].temperature.push(el.main.temp)
      }
    });
  });
  Object.values(temp).forEach(el=>{
    const maxTemperature = Math.max.apply(null, el.temperature);
    const minTemperature = Math.min.apply(null, el.temperature);
    el.temperature = [maxTemperature, minTemperature];
  });
  return temp;
}

